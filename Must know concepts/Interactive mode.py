def sum(x, t):
    """Adds two numbers together.

    Args:
        x (int or float): The first number.
        t (int or float): The second number.

    Returns:
        int or float: The sum of x and t.
    """
    return x + t

# Example usage
result = sum(1, 2)
print(result)  # Output: 3

